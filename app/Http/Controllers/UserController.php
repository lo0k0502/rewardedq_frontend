     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $newUser = new user;
        $newUser->name = $request->input('user.name');
        $newUser->stuid = $request->input('user.stuid');
        $newUser->depGrade = $request->input('user.depGrade');
        $newUser->grade = $request->input('user.grade');
        $newUser->save();

        return $newUser;
    }

